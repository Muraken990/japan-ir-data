# WordPress REST API設定
WORDPRESS_API_URL = "https://japanir.jp/wp-json/wp/v2/company"
REQUEST_TIMEOUT = 30  # タイムアウト（秒）
MAX_RETRIES = 3  # リトライ回数
MAX_PAGES = 50  # 最大取得ページ数（100社/ページ × 50 = 最大5000社）

# 出力ファイル
SCRIPT_DIR = os.path.dirname(os.path.abspath(__file__))
PROJECT_ROOT = os.path.dirname(SCRIPT_DIR)
OUTPUT_FILE = os.path.join(PROJECT_ROOT, "data", "wordpress_companies.csv")

# ============================================
# WordPress REST APIから企業リストを取得
# ============================================

def fetch_wordpress_companies():
    """
    WordPress REST APIから登録済み企業を取得

    Returns:
        list: 企業データのリスト [{code, name, ticker}, ...]
    """
    print("=" * 60)
    print("WordPress連携: 登録済み企業リスト取得")
    print("=" * 60)
    print(f"API URL: {WORDPRESS_API_URL}")
    print()

    all_companies = []
    offset = 0
    per_page = 100  # 1回あたりの取得数
    max_companies = MAX_PAGES * per_page  # 最大5000社

    while offset < max_companies:
        batch_num = (offset // per_page) + 1
        print(f"📥 バッチ {batch_num} を取得中（offset: {offset}）...")

        params = {
            "per_page": per_page,
            "offset": offset,  # pageの代わりにoffsetを使用
            "_fields": "id,title,stock_code",  # 必要なフィールドのみ取得
            "status": "publish"  # 公開済みのみ
        }

        # リトライ処理
        for attempt in range(1, MAX_RETRIES + 1):
            try:
                response = requests.get(
                    WORDPRESS_API_URL,
                    params=params,
                    timeout=REQUEST_TIMEOUT
                )

                if response.status_code == 200:
                    companies = response.json()

                    if not companies:
                        print(f"✅ 全データ取得完了（offset {offset} まで、バッチ {batch_num - 1} 完了）")
                        return all_companies

                    # データを抽出
                    for company in companies:
                        try:
                            # stock_codeフィールドから証券コードを取得
                            code = company.get('stock_code', '')

                            # stock_codeが存在する場合のみ追加
                            if code:
                                # 4桁の英数字を許可（例: 7203, 446A）
                                if isinstance(code, str) and len(code) == 4 and code.isalnum():
                                    all_companies.append({
                                        'code': code,
                                        'name': company.get('title', {}).get('rendered', ''),
                                        'ticker': f"{code}.T"  # yfinance形式に変換
                                    })
                                else:
                                    print(f"  ⚠️  無効な証券コード: {code} (ID: {company.get('id')})")
                        except Exception as e:
                            print(f"  ❌ データ解析エラー (ID: {company.get('id')}): {str(e)}")
                            continue

                    print(f"  ✅ {len(companies)}件取得（累計: {len(all_companies)}社）")
                    offset += per_page
                    time.sleep(0.5)  # レート制限対策
                    break

                elif response.status_code == 400:
                    # データ範囲外（最終データ到達）
                    print(f"✅ 全データ取得完了（バッチ {batch_num} まで）")
                    return all_companies

                else:
                    print(f"  ⚠️  HTTPエラー: {response.status_code}")
                    if attempt < MAX_RETRIES:
                        print(f"  🔄 リトライ {attempt}/{MAX_RETRIES}...")
                        time.sleep(2 ** attempt)  # 指数バックオフ
                    else:
                        print(f"  ❌ {MAX_RETRIES}回リトライ失敗")
                        return all_companies

            except requests.exceptions.Timeout:
                print(f"  ⏱️  タイムアウト")
                if attempt < MAX_RETRIES:
                    print(f"  🔄 リトライ {attempt}/{MAX_RETRIES}...")
                    time.sleep(2 ** attempt)
                else:
                    print(f"  ❌ {MAX_RETRIES}回リトライ失敗")
                    return all_companies

            except Exception as e:
                print(f"  ❌ リクエストエラー: {str(e)}")
                if attempt < MAX_RETRIES:
                    print(f"  🔄 リトライ {attempt}/{MAX_RETRIES}...")
                    time.sleep(2 ** attempt)
                else:
                    print(f"  ❌ {MAX_RETRIES}回リトライ失敗")
                    return all_companies

    # 最大取得数に到達
    if offset >= max_companies:
        print()
        print(f"⚠️  最大取得数 ({max_companies}社) に到達しました")
        print(f"📊 取得企業数: {len(all_companies)}社")

    return all_companies


def save_to_csv(companies):
    """
    企業リストをCSVファイルに保存

    Args:
        companies (list): 企業データのリスト

    Returns:
        bool: 保存成功時True
    """
    print()
    print("=" * 60)
    print("CSVファイル保存")
    print("=" * 60)

    if not companies:
        print("❌ 保存する企業データがありません")
        return False

    try:
        # dataディレクトリが存在しない場合は作成
        os.makedirs(os.path.dirname(OUTPUT_FILE), exist_ok=True)

        # CSVに保存
        with open(OUTPUT_FILE, 'w', encoding='utf-8', newline='') as f:
            writer = csv.DictWriter(f, fieldnames=['code', 'name', 'ticker'])
            writer.writeheader()
            writer.writerows(companies)

        print(f"✅ 保存完了: {OUTPUT_FILE}")
        print(f"📊 企業数: {len(companies)}社")

        # サンプル表示（最初の5社）
        print()
        print("📋 サンプル（最初の5社）:")
        for i, company in enumerate(companies[:5], 1):
            print(f"  {i}. {company['code']} - {company['name']} ({company['ticker']})")

        return True

    except Exception as e:
        print(f"❌ CSV保存エラー: {str(e)}")
        return False


